function superficies
figure('MenuBar','none','NumberTitle','off',...
    'Name','GRAFLAB - Superficies',...
    'Position',[300 300 650 350],'resize','off');
centerfig();

ax=axes('Units','pixels','Position',[35 30 400 300]);
surf(NaN(2));

p=uipanel(gcf,'units','pixels','Position',[470 20 170 310],...
    'BackgroundColor',get(gcf,'color')*0.9);

uicontrol(p,'style','text','String','Función',...
    'Position',[10 280 150 20],...
    'BackgroundColor',get(p,'BackgroundColor'));

uicontrol(p,'style','text','String','f(x,y)',...
    'Position',[4 255 30 20],'FontSize',10,...
    'BackgroundColor',get(p,'BackgroundColor'));
hf=uicontrol(p,'style','edit','String','cos(x)+sin(y)',...
    'Position',[37 255 125 20]);


uicontrol(p,'style','text','String','Intervalos',...
    'Position',[10 210 150 20],...
    'BackgroundColor',get(p,'BackgroundColor'));

uicontrol(p,'style','text','String','x',...
    'Position',[20 185 20 20],'FontSize',10,...
    'Backgroundcolor',get(p,'BackgroundColor'));
hx1=uicontrol(p,'style','edit','String','-5',...
    'Position',[45 185 40 20]);
hx2=uicontrol(p,'style','edit','String','5',...
    'Position',[95 185 40 20]);

uicontrol(p,'style','text','String','y',...
    'Position',[20 155 20 20],'FontSize',10,...
    'Backgroundcolor',get(p,'BackgroundColor'));
hy1=uicontrol(p,'style','edit','String','-5',...
    'Position',[45 155 40 20]);
hy2=uicontrol(p,'style','edit','String','5',...
    'Position',[95 155 40 20]);

uicontrol(p,'style','text','String','Mapa de color',...
    'Position',[10 110 150 20],...
    'BackgroundColor',get(p,'BackgroundColor'));
hcm=uicontrol(p,'style','popupmenu',...
    'String',{'hsv','jet','hot','gray','pink',...
    'cool','autumn','Aleatorio'},...
    'Position',[40 85 90 22]);


uicontrol(p,'style','push','String','Graficar',...
    'Position',[40 30 90 22],'FontSize',10,...
    'FontWeight','b','Callback',@graficar);

    function graficar(~,~)
        f=get(hf,'String');
        x1=str2double(get(hx1,'String'));
        x2=str2double(get(hx2,'String'));
        y1=str2double(get(hy1,'String'));
        y2=str2double(get(hy2,'String'));
        cm=get(hcm,'String');
        k=get(hcm,'value');
        switch cm{k}
            case 'Aleatorio'
                colormap(rand(64,3));
            otherwise
                colormap(cm{k});
        end
        ezsurf(f,[x1 x2 y1 y2]);
    end
end