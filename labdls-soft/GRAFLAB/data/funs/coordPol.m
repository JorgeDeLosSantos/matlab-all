function coordPol
figure('MenuBar','none','NumberTitle','off',...
    'Name','GRAFLAB - Coordenadas Polares',...
    'Position',[300 300 500 400],'resize','off');
centerfig();

ax=axes('Units','pixels','Position',[30 70 440 320]);
plot(NaN);

p=uipanel(gcf,'units','pixels','Position',[30 8 440 40],...
    'BackgroundColor',get(gcf,'color')*0.9);

uicontrol(p,'style','text','String','r (t)',...
    'Position',[10 10 30 20],'FontSize',10,...
    'BackgroundColor',get(p,'BackgroundColor'));
hfun=uicontrol(p,'style','edit','String','',...
    'Position',[45 10 100 20]);

uicontrol(p,'style','text','String','a',...
    'Position',[160 10 20 20],'FontSize',10,...
    'Backgroundcolor',get(p,'BackgroundColor'));
ha=uicontrol(p,'style','edit','String','',...
    'Position',[180 10 40 20]);

uicontrol(p,'style','text','String','b',...
    'Position',[225 10 20 20],'FontSize',10,...
    'Backgroundcolor',get(p,'BackgroundColor'));
hb=uicontrol(p,'style','edit','String','',...
    'Position',[245 10 40 20]);

uicontrol(p,'style','push','String','Graficar',...
    'Position',[330 9 80 22],'FontSize',10,...
    'FontWeight','b','Callback',@graficar);

    function graficar(~,~)
        f=get(hfun,'String');
        a=str2double(get(ha,'String'));
        b=str2double(get(hb,'String'));
        ezpolar(f,[a b]);
    end
end