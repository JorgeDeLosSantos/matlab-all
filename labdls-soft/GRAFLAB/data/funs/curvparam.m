function curvparam
figure('MenuBar','none','NumberTitle','off',...
    'Name','GRAFLAB - Curvas Paramétricas',...
    'Position',[300 300 650 350],'resize','off');
centerfig();

ax=axes('Units','pixels','Position',[35 30 400 300]);
surf(NaN(2));

p=uipanel(gcf,'units','pixels','Position',[470 20 170 310],...
    'BackgroundColor',get(gcf,'color')*0.9);

uicontrol(p,'style','text','String','Ecuaciones paramétricas',...
    'Position',[10 280 150 20],...
    'BackgroundColor',get(p,'BackgroundColor'));

uicontrol(p,'style','text','String','x(t)',...
    'Position',[4 255 30 20],'FontSize',10,...
    'BackgroundColor',get(p,'BackgroundColor'));
hxt=uicontrol(p,'style','edit','String','cos(t)',...
    'Position',[37 255 125 20]);

uicontrol(p,'style','text','String','y(t)',...
    'Position',[4 220 30 20],'FontSize',10,...
    'BackgroundColor',get(p,'BackgroundColor'));
hyt=uicontrol(p,'style','edit','String','sin(t)',...
    'Position',[37 220 125 20]);

uicontrol(p,'style','text','String','z(t)',...
    'Position',[4 185 30 20],'FontSize',10,...
    'BackgroundColor',get(p,'BackgroundColor'));
hzt=uicontrol(p,'style','edit','String','t',...
    'Position',[37 185 125 20]);


uicontrol(p,'style','text','String','Intervalo',...
    'Position',[10 120 150 20],...
    'BackgroundColor',get(p,'BackgroundColor'));

uicontrol(p,'style','text','String','t',...
    'Position',[20 95 20 20],'FontSize',10,...
    'Backgroundcolor',get(p,'BackgroundColor'),...
    'FontWeight','b');
ht1=uicontrol(p,'style','edit','String','0',...
    'Position',[45 95 40 20]);
ht2=uicontrol(p,'style','edit','String','5',...
    'Position',[95 95 40 20]);



uicontrol(p,'style','push','String','Graficar',...
    'Position',[40 30 90 22],'FontSize',10,...
    'FontWeight','b','Callback',@graficar);

    function graficar(~,~)
        x=get(hxt,'String');
        y=get(hyt,'String');
        z=get(hzt,'String');
        t1=str2double(get(ht1,'String'));
        t2=str2double(get(ht2,'String'));
        ezplot3(x,y,z,[t1 t2]);
    end
end